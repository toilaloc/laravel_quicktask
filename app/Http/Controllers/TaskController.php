    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreTaskRequest $request)
    {

        $storeDataTask = $request->all();
        $storeTask = Auth::user()->tasks()->create($storeDataTask);
        if ($storeTask) {

            return back()->withSuccess('message.added');
        }

        return back()->withError('message.fail_added');
    }


